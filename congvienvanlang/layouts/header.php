<p style="text-align: justify;">Sân vận động, bể bơi, nhà thi đấu mới, được xây dựng khang trang bên cạnh Công viên Văn Lang. Nhiều hoạt động thể dục, thể thao truyền thống như đi bộ, bơi chải&#8230; được khuyến khích tổ chức, phục vụ khách du lịch vui chơi, tham quan.</p><div class="jeg_ad jeg_ad_article jnews_content_inline_ads  "><div class='ads-wrapper align-center '><div class='ads_code'><script async src="https://pagead2.googlesyndication.com/pagead/js/adsbygoogle.js"></script>
