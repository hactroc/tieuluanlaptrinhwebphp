<p style="text-align: justify;">Cầu có chiều dài 178m, nối liền hai bờ hồ với điểm nhấn là một tòa tháp cao 7 tầng được xây dựng giữa hồ và trang trí bằng quả cầu kính chiếu sáng. Xung quanh hồ được lắp hệ thống đèn chiếu sáng hiện đại, rực rỡ sắc màu.</p>
