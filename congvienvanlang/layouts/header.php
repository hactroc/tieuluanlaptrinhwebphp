<p style="text-align: justify;">Trên hồ nước có hai đảo nhỏ: đảo Mai An Tiêm và đảo Tiên Dung &#8211; Chử Đồng Tử. Đảo Mai An Tiêm nổi ở giữa nhánh hồ tây nam, trên đảo có biểu tượng hình quả dưa hấu. Lâu đài của Tiên Dung và Chử Đồng Tử được xây dựng trên một đảo nhỏ giữa hồ ẩn hiện qua làn sương mù mỗi buổi sớm, khói lam chiều.</p>
<h2 style="text-align: justify;"><span class="ez-toc-section" id="Nhung_truyen_thuyet_ve_thoi_Van_Lang_cung_duoc_phuc_dung_tai_day"></span><strong>Những truyền thuyết về <a href="">thời Văn Lang</a> cũng được phục dựng tại đây</strong><span class="ez-toc-section-end"></span></h2>
<p style="text-align: justify;">Đi vào dải đất phía đông, nhánh hồ khu vực Tiên Sơn, một cây cau thân thẳng vút lên trời, dưới gốc có một tảng đá hình người và một khóm trầu không quấn quýt quanh tảng đá, gốc cau gợi nhớ đến sự tích trầu cau.</p>
<p style="text-align: justify;">Đi tiếp chúng ta sẽ bắt gặp hạt thóc thần được mô tả to như chiếc thuyền nan. Hạt thóc bằng gốm màu được dựng lên trên thảm hoa văn Lạc Việt.</p>
<p style="text-align: justify;">Bên cạnh đó là những chiếc bánh chưng và những cặp bánh dày khổng lồ soi bóng lung linh trên mặt nước.</p>
